package com.pojo;

public class DevUser {
	private int devId;         //id
	private String devCode;    //账号
	private String devName;    //账号名
	private String devPassword;//密码
	/**
	 * @return the devId
	 */
	public int getDevId() {
		return devId;
	}
	/**
	 * @param devId the devId to set
	 */
	public void setDevId(int devId) {
		this.devId = devId;
	}
	/**
	 * @return the devCode
	 */
	public String getDevCode() {
		return devCode;
	}
	/**
	 * @param devCode the devCode to set
	 */
	public void setDevCode(String devCode) {
		this.devCode = devCode;
	}
	/**
	 * @return the devName
	 */
	public String getDevName() {
		return devName;
	}
	/**
	 * @param devName the devName to set
	 */
	public void setDevName(String devName) {
		this.devName = devName;
	}
	/**
	 * @return the devPassword
	 */
	public String getDevPassword() {
		return devPassword;
	}
	/**
	 * @param devPassword the devPassword to set
	 */
	public void setDevPassword(String devPassword) {
		this.devPassword = devPassword;
	}
	

}
